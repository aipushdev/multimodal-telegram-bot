import telebot

WELCOME = """Привет. Я рядом.

Здесь можно говорить о том, что происходит внутри — без спешки и осуждения. Я не даю советов и не оцениваю. Я помогаю исследовать то, что ты чувствуешь.

Вот что я умею:

/session — полноценная сессия на 60 минут. Можно говорить голосом, писать текст или прислать фото страницы из дневника. Я буду рядом всё это время.

/diary — короткая встреча на 30 минут. Просто расскажи, как прошёл день, что зацепило, что осталось внутри.

/card — я создам для тебя образ на основе последних наших встреч. Метафорическую карту. Ты сможешь поработать с ней, если захочешь.

/report — сводка за период. Что мы исследовали, что повторялось, что менялось.

Когда будешь готов — просто выбери с чего начать."""


def register(bot: telebot.TeleBot) -> None:
    @bot.message_handler(commands=["start"])
    def cmd_start(message):
        bot.send_message(message.chat.id, WELCOME)
